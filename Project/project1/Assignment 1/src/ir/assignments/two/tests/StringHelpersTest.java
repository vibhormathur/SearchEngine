package ir.assignments.two.tests;

import static org.junit.Assert.*;
import ir.assignments.two.d.StringHelpers;

import org.junit.Test;

public class StringHelpersTest {

	@Test
	public void testIsPalindrome() {
		// Positive
		assertEquals(true, StringHelpers.isPalindrome("kayak"));
		assertEquals(true, StringHelpers.isPalindrome("abba"));
		assertEquals(true, StringHelpers.isPalindrome("bat tab"));
		assertEquals(true, StringHelpers.isPalindrome("do geese see god"));
		
		// Negative
		assertEquals(false, StringHelpers.isPalindrome("car"));
		assertEquals(false, StringHelpers.isPalindrome("four"));
		assertEquals(false, StringHelpers.isPalindrome("this is a sentence"));		
	}

}
